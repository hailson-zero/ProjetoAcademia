package Model;

/**
 *
 * @author tylui
 */
public class CadastroEquipamentoModel {
    private int codigoEquipamento;
    private String descricaoEquipamento;
    private String marcaEquipamento;
    private String modeloEquipamento;
    private String pesoEquipamento;
    private String tipoEquipamento;
    private String danificadoEquipamento;

    public int getCodigoEquipamento() {
        return codigoEquipamento;
    }

    public void setCodigoEquipamento(int codigoEquipamento) {
        this.codigoEquipamento = codigoEquipamento;
    }

    public String getDescricaoEquipamento() {
        return descricaoEquipamento;
    }

    public void setDescricaoEquipamento(String descricaoEquipamento) {
        this.descricaoEquipamento = descricaoEquipamento;
    }

    public String getMarcaEquipamento() {
        return marcaEquipamento;
    }

    public void setMarcaEquipamento(String marcaEquipamento) {
        this.marcaEquipamento = marcaEquipamento;
    }

    public String getModeloEquipamento() {
        return modeloEquipamento;
    }

    public void setModeloEquipamento(String modeloEquipamento) {
        this.modeloEquipamento = modeloEquipamento;
    }

    public String getPesoEquipamento() {
        return pesoEquipamento;
    }

    public void setPesoEquipamento(String pesoEquipamento) {
        this.pesoEquipamento = pesoEquipamento;
    }

    public String getTipoEquipamento() {
        return tipoEquipamento;
    }

    public void setTipoEquipamento(String tipoEquipamento) {
        this.tipoEquipamento = tipoEquipamento;
    }

    public String getDanificadoEquipamento() {
        return danificadoEquipamento;
    }

    public void setDanificadoEquipamento(String danificadoEquipamento) {
        this.danificadoEquipamento = danificadoEquipamento;
    }
}
