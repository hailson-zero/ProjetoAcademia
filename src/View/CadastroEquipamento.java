    private void jbSalvarEquipeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarEquipeActionPerformed
        // TODO add your handling code here:
        if (!Editar){
            if((!jtxDescricaoEquipe.getText().trim().equals(""))&& (jrSimEquipe.isSelected() || jrNaoEquipe.isSelected())){
                CadastroEquipamentoModel equipamento = new CadastroEquipamentoModel();
                equipamento.setDescricaoEquipamento(jtxDescricaoEquipe.getText());
                equipamento.setMarcaEquipamento(jtxMarcaEquipe.getText());
                equipamento.setModeloEquipamento(jtxModeloEquipe.getText());
                equipamento.setPesoEquipamento(jtxPesoEquipe.getText());
                equipamento.setTipoEquipamento(jtxTipoUsoEquipe.getText());
                if (jrSimEquipe.isSelected())
                    equipamento.setDanificadoEquipamento(jrSimEquipe.getText());
                if (jrNaoEquipe.isSelected())
                    equipamento.setDanificadoEquipamento(jrNaoEquipe.getText());
                EquipamentoController controle = new EquipamentoController();
                if(controle.inserirEquipamento(equipamento)){
                    JOptionPane.showMessageDialog(this, "Equipamento salvo com sucesso!");
                    jbLimparEquipeActionPerformed(evt);
                }else{
                    JOptionPane.showMessageDialog(this, "Erro ao salvar Equipamento!", 
                            "Retorno", JOptionPane.ERROR_MESSAGE);
                }       
           }else{
                JOptionPane.showMessageDialog(this, "Erro ao Cadastrar Equipamento! Campos Incompletos", 
                            "Retorno", JOptionPane.ERROR_MESSAGE);
            }
            
        }else{
            if((!jtxDescricaoEquipe.getText().trim().equals(""))&& (jrSimEquipe.isSelected() || jrNaoEquipe.isSelected())){
                CadastroEquipamentoModel equipamento = new CadastroEquipamentoModel();
                equipamento.setCodigoEquipamento(CodigoEditEquip);
                equipamento.setDescricaoEquipamento(jtxDescricaoEquipe.getText());
                equipamento.setMarcaEquipamento(jtxMarcaEquipe.getText());
                equipamento.setModeloEquipamento(jtxModeloEquipe.getText());
                equipamento.setPesoEquipamento(jtxPesoEquipe.getText());
                equipamento.setTipoEquipamento(jtxTipoUsoEquipe.getText());
                if (jrSimEquipe.isSelected())
                    equipamento.setDanificadoEquipamento(jrSimEquipe.getText());
                if (jrNaoEquipe.isSelected())
                    equipamento.setDanificadoEquipamento(jrNaoEquipe.getText());
                EquipamentoController controle = new EquipamentoController();
                if(controle.editarEquipamento(equipamento)){
                    JOptionPane.showMessageDialog(this, "Equipamento editado com sucesso!");
                }else{
                    JOptionPane.showMessageDialog(this, "Erro ao Editar Equipamento!", 
                            "Retorno", JOptionPane.ERROR_MESSAGE);
                }       
           }else{
                JOptionPane.showMessageDialog(this, "Erro ao Cadastrar Equipamento! Campos Incompletos", 
                            "Retorno", JOptionPane.ERROR_MESSAGE);
            }
            dispose();
            EquipamentosView equipamento = new EquipamentosView();
            equipamento.setVisible(true);
            Editar = false;
            CodigoEditEquip = 0;
        }
            
    }//GEN-LAST:event_jbSalvarEquipeActionPerformed

