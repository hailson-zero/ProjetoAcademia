    /**
     * @param args the command line arguments
     */
    void recebeDados(int CodigoEquip, boolean EditarEquip){
        CodigoEditEquip = CodigoEquip;
        Editar = EditarEquip;
        CadastroEquipamentoModel equipamento = new CadastroEquipamentoModel();
        equipamento.setCodigoEquipamento(CodigoEditEquip);
        EquipamentoController controle = new EquipamentoController();
        equipamento = controle.selecionarEquipamento(equipamento);
        if(equipamento != null){
            jtxDescricaoEquipe.setText(equipamento.getDescricaoEquipamento());
            jtxMarcaEquipe.setText(equipamento.getMarcaEquipamento());
            jtxModeloEquipe.setText(equipamento.getModeloEquipamento());
            jtxPesoEquipe.setText(equipamento.getPesoEquipamento());
            jtxTipoUsoEquipe.setText(equipamento.getTipoEquipamento());
        }else{
            JOptionPane.showMessageDialog(this, "Equipamento não encontrado!", 
                        "Retorno", JOptionPane.ERROR_MESSAGE);
        }
    }

