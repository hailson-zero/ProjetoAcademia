    public void atualizarTabela(){
        AlunoController aluno = new AlunoController();
        ArrayList<CadastroAlunoModel> lista = aluno.selecionarTodosAlunos();
        if(!lista.isEmpty()){
            DefaultTableModel modelo = (DefaultTableModel)TabelaAluno.getModel();
            modelo.setRowCount(0);
            for(CadastroAlunoModel a: lista){
                modelo.addRow(new String[]{
                    String.valueOf(a.getCodigoAluno()),
                    a.getNomeAluno(),
                    a.getSexoAluno(),
                    a.getCpfAluno(),
                    a.getRuaAluno(),
                    a.getBairroAluno(),
                    String.valueOf(a.getNumeroAluno()),
                    a.getCidadeAluno(),
                    a.getHorarioAluno(),
                    a.getEstadoAluno(),
                    String.valueOf(a.getPesoCorporal()),
                    String.valueOf(a.getEstatura())
                });
            }
        }
    }

