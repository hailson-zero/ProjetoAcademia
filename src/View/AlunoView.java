    private void jbPesquisarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarAlunoActionPerformed
        // TODO add your handling code here:
        if (!jtxPesquisaAluno.getText().trim().equals("")){
            CadastroAlunoModel aluno = new CadastroAlunoModel();
            aluno.setNomeAluno(jtxPesquisaAluno.getText());
            AlunoController controller = new AlunoController();
            ArrayList<CadastroAlunoModel> lista = controller.selecionarTodosNomesAlunos(aluno);
            if(!lista.isEmpty()){
                DefaultTableModel modelo = (DefaultTableModel)TabelaAluno.getModel();
                modelo.setRowCount(0);
                for(CadastroAlunoModel a: lista){
                    modelo.addRow(new String[]{
                        String.valueOf(a.getCodigoAluno()),
                        a.getNomeAluno(),
                        a.getSexoAluno(),
                        a.getCpfAluno(),
                        a.getRuaAluno(),
                        a.getBairroAluno(),
                        String.valueOf(a.getNumeroAluno()),
                        a.getCidadeAluno(),
                        a.getHorarioAluno(),
                        a.getEstadoAluno(),
                        String.valueOf(a.getPesoCorporal()),
                        String.valueOf(a.getEstatura())
                    });
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao Pesquisar", 
                            "Retorno", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbPesquisarAlunoActionPerformed

